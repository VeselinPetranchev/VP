package com.company;

public class Vertex {
    private int x;
    private int y;
    private boolean correctVertex=true;

    public boolean getCorrectVertex() {
        return correctVertex;
    }

    public void setCorrectVertex(boolean correctVertex) {
        this.correctVertex = correctVertex;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
